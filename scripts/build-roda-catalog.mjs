/**
 * build-roda-catalog.mjs — regenerates public/roda_catalog.json from the AWS
 * Registry of Open Data. Runs at BUILD TIME only (locally or in GitHub Actions),
 * never in the browser — the deployed site stays 100% static.
 *
 * Source: the registry's public NDJSON index (one JSON dataset record per line).
 * A runner is a server, so the index's lack of CORS headers is irrelevant here.
 *
 * Usage: node scripts/build-roda-catalog.mjs
 * Requires Node 18+ (global fetch).
 */
import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const INDEX_URL = 'https://registry.opendata.aws/roda/ndjson/index.ndjson';
const OUT = join(dirname(fileURLToPath(import.meta.url)), '..', 'public', 'roda_catalog.json');

const cleanText = (s, max = 240) => {
  const t = String(s || '')
    .replace(/```[\s\S]*?```/g, ' ')       // code fences
    .replace(/!?\[([^\]]*)\]\([^)]*\)/g, '$1') // md links/images -> text
    .replace(/[*_`>#]/g, '')
    .replace(/\s+/g, ' ')
    .trim();
  if (t.length <= max) return t;
  const cut = t.slice(0, max);
  const lastStop = Math.max(cut.lastIndexOf('. '), cut.lastIndexOf(' '));
  return (lastStop > max * 0.6 ? cut.slice(0, lastStop) : cut).trim() + '…';
};

/** arn:aws:s3:::bucket[/prefix...] -> { bucket, prefix } */
function parseS3Arn(arn) {
  const m = /^arn:aws:s3:::([^/]+)(?:\/(.*))?$/.exec(String(arn || ''));
  if (!m) return null;
  return { bucket: m[1], prefix: m[2] ? m[2].replace(/\/?$/, '/') : '' };
}

function toEntry(rec) {
  if (rec.Deprecated) return null;
  const resources = Array.isArray(rec.Resources) ? rec.Resources : [];
  // Prefer an explicit "S3 Bucket" resource; fall back to any S3 ARN.
  const s3 = resources.find(r => r.Type === 'S3 Bucket' && parseS3Arn(r.ARN))
          || resources.find(r => parseS3Arn(r.ARN));
  const arn = s3 && parseS3Arn(s3.ARN);
  if (!arn) return null; // needs a public S3 location for the engine to explore

  const tags = Array.from(new Set((rec.Tags || [])
    .map(t => String(t).toLowerCase().trim()).filter(Boolean))).slice(0, 8);

  return {
    id: rec.Slug || cleanText(rec.Name, 60).toLowerCase().replace(/[^a-z0-9]+/g, '-'),
    name: cleanText(rec.Name, 120),
    description: cleanText(rec.Description, 240),
    bucket: arn.bucket,
    region: s3.Region || 'us-east-1',
    prefix: arn.prefix,
    readme: '',
    tags,
  };
}

async function main() {
  console.log('Fetching RODA index:', INDEX_URL);
  const res = await fetch(INDEX_URL);
  if (!res.ok) throw new Error(`Index fetch failed: ${res.status}`);
  const text = await res.text();

  const seen = new Set();
  const datasets = [];
  let total = 0, skipped = 0;
  for (const line of text.split(/\r?\n/)) {
    if (!line.trim()) continue;
    total++;
    let rec;
    try { rec = JSON.parse(line); } catch { skipped++; continue; }
    const e = toEntry(rec);
    if (!e) { skipped++; continue; }
    if (seen.has(e.id)) continue;
    seen.add(e.id);
    datasets.push(e);
  }
  datasets.sort((a, b) => a.name.localeCompare(b.name));

  const out = {
    generated: new Date().toISOString().slice(0, 10),
    source: 'AWS Registry of Open Data (https://registry.opendata.aws/) — generated by scripts/build-roda-catalog.mjs',
    note: 'Auto-generated. Buckets are public; most are anonymously readable (Access-Control-Allow-Origin: *). Some may be requester-pays or region-restricted.',
    count: datasets.length,
    datasets,
  };
  await writeFile(OUT, JSON.stringify(out, null, 2) + '\n');
  console.log(`Wrote ${datasets.length} datasets to ${OUT} (from ${total} records, ${skipped} skipped without S3).`);
}

main().catch(e => { console.error(e); process.exit(1); });
